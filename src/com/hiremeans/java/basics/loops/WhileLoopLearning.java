package com.hiremeans.java.basics.loops;

public class WhileLoopLearning {

    /**
     *
     *    while(condition){
     *
     *        body/logic of the loop
     *
     *        increment/decrement
     *
     *    }
     *
     *
     *
     */


    public static void main(String[] args) {


        int x = 0;

        while(x<=10){
            System.out.println(x);
            x++;  //1
        }



    }



}
