package com.hiremeans.java.basics.loops;

public class DoWhileLoopLearning {
    /**
     *
     *   do{
     *      //body and logic
     *      /increment/decrement
     *   }while(condition);
     *
     *
     */

    public static void main(String[] args) {


        int y = 0;

        // use case is print the value from 0 to 10;

        do{
            System.out.println(y);
            y++;
        }while(y<=10);


    }




}
